package n2exercici1;

public class Dades {		
		private String direccio;
		private String telefon;
		
		public Dades (String direccio, String telefon) {
			this.direccio = direccio;
			this.telefon = telefon;
		}

		public String getDireccio() {
			return direccio;
		}
		
		public String getTelefon() {
			return telefon;
		}	
}
